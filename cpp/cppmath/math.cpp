#include <iostream>
#include <cmath>

using namespace std;

int main() {
    int x  = 5;
    int y = 10;

    cout << "Max: " << max(x, y) << " Min: " << min(x, y) << endl;

    cout << sqrt(x) << endl;// squareroot
    cout << round(sqrt(y)) << endl; // rounding to the next integer
    cout << log(2.71829) << endl; //natural logarithm
}
